//BITWISE OPERATOR
import java.util.*;
public class BIT_1{
    public static void main(String args[]){
        //BITWISE AND OPERATOR  &
        System.out.println(5&6);
        //BITWISE OR OPERATOR |
        System.out.println(5|6);
        //BITWISE XOR OPERATOR ^
        System.out.println(5^6);
        //BINARY ONE'S COMPLEMENT
        System.out.println(~5);
        System.out.println(~0);

        //BINARY LEFT SHIFT <<
        System.out.println(5<<2);
        //BINARY RIGHT SHIFT
        System.out.println(5>>2);
    }
}